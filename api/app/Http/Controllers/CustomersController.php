        $customer = Customers::where('cpf', $cpf)->get()->count();
        if($customer) {
            $cpfIsRegistered = true;
        } else {
            $cpfIsRegistered = false;
        }
        return response()->json(['cpfIsRegistered' => $cpfIsRegistered]);
